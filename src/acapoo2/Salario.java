    public void calcularSalario(Integer horasTrabajadas, Integer pagoHora) {
        NumberFormat formatoCOP = NumberFormat.getCurrencyInstance(new Locale("es", "CO"));

        if (horasTrabajadas == null || pagoHora == null) {
            System.out.println("Las horas trabajadas y el pago por hora no pueden ser nulos.");
            return;
        }

        // Calcular salario bruto
        Integer salarioBruto = horasTrabajadas * pagoHora;

        // Calcular descuento (20% del salario bruto)
        Integer descuento = (int) (salarioBruto * 0.20);

        // Calcular salario neto
        Integer salarioNeto = salarioBruto - descuento;

        // Mostrar resultados
        jTextField4.setText(""+formatoCOP.format(salarioBruto));
        jTextField5.setText(""+formatoCOP.format(descuento));
        jTextField6.setText(""+formatoCOP.format(salarioNeto));
        
    }
    
