    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        txtHamburguesa.setText("0");
            txtPapas.setText("0");
            txtGaseosa.setText("0");
            txtHotDog.setText("0");
            txtEnsalada.setText("0");
        
        if(jComboBox1.getSelectedIndex()!=0){
            txtHamburguesa.setEditable(false);
            txtPapas.setEditable(false);
            txtGaseosa.setEditable(false);
            txtHotDog.setEditable(false);
            txtEnsalada.setEditable(false);
            
            // Asignar valores según el combo seleccionado
            String comboSeleccionado=jComboBox1.getSelectedItem().toString();
            if (comboSeleccionado.equals("Hamburguesa; Papas y Gaseosa")) {
                txtHamburguesa.setText("1");
                txtPapas.setText("1");
                txtGaseosa.setText("1");
            } else if (comboSeleccionado.equals("Hamburguesa y Gaseosa")) {
                txtHamburguesa.setText("1");
                txtGaseosa.setText("1");
            } else if (comboSeleccionado.equals("Ensalada y Gaseosa")) {
                txtEnsalada.setText("1");
                txtGaseosa.setText("1");
            }
            
             
        }else{
            txtHamburguesa.setEditable(true);
            txtPapas.setEditable(true);
            txtGaseosa.setEditable(true);
            txtHotDog.setEditable(true);
            txtEnsalada.setEditable(true);
            
            
        }
        
        
        NumberFormat formatoCOP = NumberFormat.getCurrencyInstance(new Locale("es", "CO"));
        txtTotal.setText(""+formatoCOP.format(calcularTotal()));       
        
    }//GEN-LAST:event_jComboBox1ActionPerformed

