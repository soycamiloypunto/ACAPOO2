    public int calcularTotal() {
        // Precios de los productos
        final int PRECIO_HAMBURGUESA = 10000;
        final int PRECIO_PAPAS = 2000;
        final int PRECIO_GASEOSA = 4000;
        final int PRECIO_HOTDOG = 6000;
        final int PRECIO_ENSALADA = 5000;

        int total = 0;

        try {
            // Calcular total sumando cada producto según la cantidad ingresada
            if (!txtHamburguesa.getText().isEmpty()) {
                total += Integer.parseInt(txtHamburguesa.getText()) * PRECIO_HAMBURGUESA;
            }
            if (!txtPapas.getText().isEmpty()) {
                total += Integer.parseInt(txtPapas.getText()) * PRECIO_PAPAS;
            }
            if (!txtGaseosa.getText().isEmpty()) {
                total += Integer.parseInt(txtGaseosa.getText()) * PRECIO_GASEOSA;
            }
            if (!txtHotDog.getText().isEmpty()) {
                total += Integer.parseInt(txtHotDog.getText()) * PRECIO_HOTDOG;
            }
            if (!txtEnsalada.getText().isEmpty()) {
                total += Integer.parseInt(txtEnsalada.getText()) * PRECIO_ENSALADA;
            }
        } catch (NumberFormatException e) {
            System.out.println("Error en los valores de entrada");
        }

        return total;
    }

    

